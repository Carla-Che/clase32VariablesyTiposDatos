/*Años perro

Crear un programa que pida ingresar una edad y devuelva el equivalente de esas edad en años perro.*/

const edadHumana = Number (prompt("Ingrese la edad de su perro"));

const edadAnioPerros = edadHumana * 7;

alert(`La edad de su perro en años humanos es de ${edadAnioPerros} anios`);

